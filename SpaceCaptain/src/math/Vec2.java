package math;

/**
 * Klasa odpowiada za tworzenie dwu-wymiarowego wektora, potrzebnego do tworzenia fizyki poruszajacej się rakiety
 */
public class Vec2 {
    public float x,y;

    /**
     * konstruktor klasy Vec2
     * @param x określa wartosc x wektora
     * @param y określa wartosc y wektora
     */
    public Vec2(float x, float y){
        this.x=x;
        this.y=y;
    }
}
